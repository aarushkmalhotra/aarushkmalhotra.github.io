import * as React from "react"
import { Slot } from "@radix-ui/react-slot"
import { cva, type VariantProps } from "class-variance-authority"

import { cn } from "@/lib/utils"

const buttonVariants = cva(
  "inline-flex items-center justify-center gap-2 whitespace-nowrap text-sm font-medium ring-offset-background transition-all duration-200 focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50 [&_svg]:pointer-events-none [&_svg]:size-4 [&_svg]:shrink-0",
  {
    variants: {
      variant: {
        default: "bg-primary text-primary-foreground rounded-lg shadow-sm md:hover:bg-accent md:hover:text-accent-foreground md:hover:shadow-xl md:hover:shadow-accent/20",
        destructive:
          "bg-destructive text-destructive-foreground rounded-lg shadow-sm md:hover:bg-destructive/80 md:hover:shadow-xl md:hover:shadow-destructive/30",
        outline:
          "border-2 border-input bg-background rounded-lg md:hover:bg-accent md:hover:text-accent-foreground md:hover:border-accent md:hover:shadow-lg md:hover:shadow-accent/10",
        secondary:
          "bg-secondary text-secondary-foreground rounded-lg shadow-sm md:hover:bg-accent md:hover:text-accent-foreground md:hover:shadow-lg md:hover:shadow-accent/10",
        ghost: "rounded-lg md:hover:bg-accent md:hover:text-accent-foreground md:hover:shadow-md",
        link: "text-primary underline-offset-4 rounded-sm md:hover:underline md:hover:text-accent",
      },
      size: {
        default: "h-10 px-4 py-2",
        sm: "h-9 px-3 text-xs",
        lg: "h-12 px-8 text-base",
        icon: "h-10 w-10",
      },
    },
    defaultVariants: {
      variant: "default",
      size: "default",
    },
  }
)

export interface ButtonProps
  extends React.ButtonHTMLAttributes<HTMLButtonElement>,
    VariantProps<typeof buttonVariants> {
  asChild?: boolean
}

const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant, size, asChild = false, ...props }, ref) => {
    const Comp = asChild ? Slot : "button"
    return (
      <Comp
        className={cn(buttonVariants({ variant, size, className }))}
        ref={ref}
        {...props}
      />
    )
  }
)
Button.displayName = "Button"

export { Button, buttonVariants }
